##   逻辑运算符
# and、or、not
# and运算符：两个条件都为真，结果才为真。&&
# or运算符：两个条件有一个为真，结果就为真。||
# not运算符：取反，如果条件为真，结果为假，如果条件为假，结果为真。

##短路逻辑和运算符的优先级
#短路逻辑：从左往右第一个操作数的值无法确定计算结果时，才对第二个值进行求值。
# 3 and 4 #4
# 3 or 4 #3/
